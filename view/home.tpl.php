		<h4>What is D3Bit?</h4>
		<p>
			D3Bit is an utility software for Diablo III. Its major use includes in-game item tooltip scanning/parsing. It uses OCR technology (Tesseract, to be specific) for parsing item affixes. It also has a handful of neat features that will help Diablo III players in their adventures.
		</p>
		<h4>Features</h4>
		<ul>
			<li>Crop in-game item tooltip.</li>
			<li>Parse item tooltip for item quality, type, iLvl, and of course affixes.</li>
			<li>Show you strengths of parsed stats based on the known max value for the particular stat.</li>
			<li>Upload the parsed item info to D3Bit.com for you to easily share with other players.</li>
		</ul>
		<h4>Usage</h4>
		<p>
			Your downloaded file will be a zip. Just unzip and put the content anywhere on your harddrive. If you put it under C:\, then you may need to run the program as admin. 
		</p>
		<p>
			To scan in-game tooltips, you just need to have D3Bit running in the background and press the Scan key (default is F12) while hovering your mouse on an item in-game. The game needs to be run in Fullscreen Windowed mode or Windowed mode.
		</p>
		<p>
			As far as the program interface goes, most controls on the D3Bit GUI have tooltips. So, it should be straight-forward to figure out what does what on your own.
		</p>
		<h4>Limitations</h4>
		<p>
			Right now, it's only for Windows. Porting it to Mac is actually very doable (with mono) but I currently lack the time. Also I should mention that the accuracy on parsing affixes should be very accurate on most setups. Item name parsing, however, is not too accurate. 
		</p>
		<p>
			D3Bit is very early in development, so expect lots of bugs and problems. I will do my best to fix all of them.
		</p>
